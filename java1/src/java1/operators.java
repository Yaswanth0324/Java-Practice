package java1;

public class operators {
	public static void main(String[] args) {
		int a=64;
		int b=4;
		System.out.println(a+b);
		System.out.println(a-b);    //arithmetic operators
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		System.out.println(a++);
		System.out.println(a--);
		System.out.println(a*=2);      //assignment operators
		System.out.println(b+=65);
		System.out.println(a>b);   //comparision operators
		System.out.println(a!=b);
		System.out.println(a==b);
		System.out.println(a<65&&a>30);    //logical operators
		System.out.println(b>25||b<5);
	}

}
